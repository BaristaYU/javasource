package ch1;

public class VarFloatEx1 {
    public static void main(String[] args) {
        float score = 90.10f;
        double score2 = 90d;

        System.out.println("score = " + score);
        System.out.println("score2 = " + score2);

        int value1 = 92;
        double score3 = value1;
        System.out.println(score3);
    }
}
