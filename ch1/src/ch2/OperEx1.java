package ch2;

public class OperEx1 {
    public static void main(String[] args) {
        int num1 = 25, num2 = 10;

        System.out.printf("%d + %d = %d\n", num1, num2, (num1 + num2));
        System.out.printf("%d - %d = %d\n", num1, num2, (num1 - num2));
        System.out.printf("%d * %d = %d\n", num1, num2, (num1 * num2));
        System.out.printf("%d / %d = %f\n", num1, num2, (float) num1 / (float) num2);
    }
}
